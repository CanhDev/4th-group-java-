    private void DeleteBtnTourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnTourActionPerformed
        dongChon = TourTable.getSelectedRow();
        if(dongChon != -1){
            int confirm = JOptionPane.showConfirmDialog(
                    rootPane,
                    "Bạn có chắc chắn muốn xóa?",
                    "Xác nhận xóa",
                    JOptionPane.YES_NO_OPTION
            );
            if(confirm == JOptionPane.YES_OPTION){
                    Tour removeTour = this.getElementAt(dongChon);
                    for(DonHang d: dsDH){
                    if(d.getMaTour().equals(removeTour.getMaTour())){
                        dsDH.remove(d);
                        controllerDH.writeToFile(dsDH, "QLDonHang.txt");
                        break;
                    }
                }
                dsTour.remove(removeTour);
                QuanLyTourController.writeToFile(dsTour, "Tour_List.txt");
                loadTableTour();
            }
        }
        else if(dsTour.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Không có dữ liệu trong bảng!");
        }
        else{
            JOptionPane.showMessageDialog(this, 
                    "Chưa chọn dòng xóa","Thông báo", WIDTH);
        }
    }//GEN-LAST:event_DeleteBtnTourActionPerformed

