    public Tour getElementAt(int index) {
            Iterator<Tour> it = dsTour.iterator();
            int currentIndex = 0;
            while (it.hasNext()) {
                Tour new_t = it.next();
                if (currentIndex == index) {
                    return new_t;
                }
                currentIndex++;
            }
            return null;
        }
    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        LinkedHashSet<Tour> list = new LinkedHashSet<>();
        String searchString = txtSearchString.getText().trim();
        if(searchString.isEmpty()){
            JOptionPane.showMessageDialog(this, 
                    "Chưa nhập thông tin tìm kiếm","Thông báo", WIDTH);
        }
        else{
            list = QuanLyTourController.SearchTours(dsTour, searchString);
            if(list.isEmpty()){
                JOptionPane.showMessageDialog(rootPane, "Không tìm thấy tour!");
            }
            else{
                this.loadTableTourBySearchString(list);
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

