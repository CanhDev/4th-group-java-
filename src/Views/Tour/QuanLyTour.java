    public void addTour(Tour new_t){
        try{
            dsTour.add(new_t);
            QuanLyTourController.writeToFile(dsTour, "Tour_List.txt");
            loadTableTour();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, 
                    "Lỗi không ghi ghi được file","Thông báo", WIDTH);
        }
    }
    public void EditTour(Tour new_t){
        try{
            String id = new_t.getMaTour();
            Tour editTour = new Tour();
            for(Tour i : dsTour){
                if(i.getMaTour().contains(id)){
                    editTour = i;
                    break;
                }
            }
            editTour.setTenTour(new_t.getTenTour());
            editTour.setGiaTien(new_t.getGiaTien());
            editTour.setDiaDiem(new_t.getDiaDiem());
            editTour.setDanhGia(new_t.getDanhGia());
            editTour.setLichTrinh(new_t.getLichTrinh());
            //
            for(DonHang d: dsDH){
                if(d.getMaTour().contains(editTour.getMaTour())){
                    d.setTour(editTour);
                    controllerDH.writeToFile(dsDH, "QLDonHang.txt");
                    break;
                }
            }
            QuanLyTourController.writeToFile(dsTour, "Tour_List.txt");
            loadTableTour();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, 
                    "Lỗi không ghi ghi được file","Thông báo", WIDTH);
        }
    }
    private void setTableAlignmentCenter(javax.swing.JTable table) {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.LEFT);
        
        for (int i = 0; i < table.getColumnCount(); i++) {
            table.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
    }
    private void setTableHeaderAlignmentCenter(javax.swing.JTable table) {
        DefaultTableCellRenderer headerRenderer = (DefaultTableCellRenderer) table.getTableHeader().getDefaultRenderer();
        headerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        table.getTableHeader().setDefaultRenderer(headerRenderer);
        table.getTableHeader().setFont(table.getTableHeader().getFont().deriveFont(java.awt.Font.BOLD));
    }
    
